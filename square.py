def square_area(a):
    #возвращает площадь квадрата.
    # Параметры:
    # a: длина стороны квадрата
    # Возвращаемое значение:
    # число, являющееся произведением а на а.
    if a <= 0:
        return "wrong input"
    return a * a


def square_perimeter(a):
    #возвращает периметр квадрата.
    # Параметры:
    # a: длина стороны квадрата
    # Возвращаемое значение:
    # число, являющееся произведением а на 4.
    if a <= 0 :
        return "wrong input"
