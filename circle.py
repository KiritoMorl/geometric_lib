import math


def circle_area(r):
    # возвращает площадь круга.
    # Параметры:
    # r: радиус круга
    # Возвращаемое значение:
    # число, являющееся произведением числа пи на r в квадрате.
    return math.pi * r * r

print(circle_area(2))

def circle_perimeter(r):
    # возвращает периметр круга.
    # Параметры:
    # r: радиус круга
    # Возвращаемое значение:
    # число, являющееся произведением числа пи на r и 2.
    return 2 * math.pi * r

